public class Texto {

    private String autortxt;
    private String descricaotxt;
    private String titulotxt;
    private String textotxt;

    public Texto() {
    }

    public String getAutortxt() {
        return autortxt;
    }

    public void setAutortxt(String autortxt) {
        this.autortxt = autortxt;
    }

    public String getDescricaotxt() {
        return descricaotxt;
    }

    public void setDescricaotxt(String descricaotxt) {
        this.descricaotxt = descricaotxt;
    }

    public String getTitulotxt() {
        return titulotxt;
    }

    public void setTitulotxt(String titulotxt) {
        this.titulotxt = titulotxt;
    }

    public String getTextotxt() {
        return textotxt;
    }

    public void setTextotxt(String textotxt) {
        this.textotxt = textotxt;
    }
}
